// Assignment operator

const num1 = 7;
const num2 = 2;

console.log(`"num1" value is ${num1} and "num2" value is ${num2}.\n`);

// Addition
const addition = num1 + num2;

console.log(`"num1 + num2" value is ${addition}`);

// Subtraction
const subtraction = num1 - num2;

console.log(`"num1 - num2" value is ${subtraction}`);

// Multiplication
const multiplication = num1 * num2;

console.log(`"num1 * num2" value is ${multiplication}`);

// Division
const division = num1 / num2;

console.log(`"num1 / num2" value is ${division}`);

// Remainder
const remainder = num1 % num2;

console.log(`"num1 % num2" value is ${remainder}`);